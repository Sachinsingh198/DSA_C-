#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;
int main(){
  vector<int> v(5);
  for(int i= 0; i<v.size(); i++){
    cin>>v[i];
}
for(int i= 0; i<v.size(); i++){
    cout<<v[i]<<"  ";
}
cout<<endl;
sort(v.begin(), v.end());//array will sorted in ascending order
for(int i= 0; i<v.size(); i++){
    cout<<v[i]<<"  ";
}
cout<<endl;
reverse(v.begin(), v.end());//array will sorted in descending order
for(int i= 0; i<v.size(); i++){
    cout<<v[i]<<"  ";
}
return 0;
}